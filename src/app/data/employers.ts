export const EMPLOYER_SECTORS = [...new Set(EMPLOYERS.map((e) => e.sector))]

export function getEmployerById(id: string): Employer | undefined {
  return EMPLOYERS.find((e) => e.id === id)
}

export function getEmployersBySector(): Record<string, Employer[]> {
  const result: Record<string, Employer[]> = {}
  for (const emp of EMPLOYERS) {
    if (!result[emp.sector]) result[emp.sector] = []
    result[emp.sector].push(emp)
  }
  return result
}

export function getTopEmployersForInstitution(instId: string, n = 5): Employer[] {
  return EMPLOYERS
    .filter((e) => e.top_universities.some((u) => u.id === instId))
    .sort((a, b) => {
      const aH = a.top_universities.find((u) => u.id === instId)?.annual_hires ?? 0
      const bH = b.top_universities.find((u) => u.id === instId)?.annual_hires ?? 0
      return bH - aH
    })
    .slice(0, n)
}
