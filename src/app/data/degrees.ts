export const DEGREE_SECTORS = [...new Set(DEGREES.flatMap((d) => d.industry_destinations.map((i) => i.sector)))]

export function getDegreeById(id: string): Degree | undefined {
  return DEGREES.find((d) => d.id === id)
}

export function getDegreesByAIRisk(order: 'asc' | 'desc' = 'desc'): Degree[] {
  return [...DEGREES].sort((a, b) =>
    order === 'desc'
      ? b.ai_automation_risk_pct - a.ai_automation_risk_pct
      : a.ai_automation_risk_pct - b.ai_automation_risk_pct,
  )
}

export function getDegreesForInstitution(instId: string): Degree[] {
  return DEGREES.filter((d) => d.top_institutions.includes(instId))
}

export function getSectorDegreeStats() {
  const n = DEGREES.length
  return {
    total_subjects: n,
    avg_employment_rate: +(DEGREES.reduce((s, d) => s + d.employment_rate_pct, 0) / n).toFixed(1),
    avg_salary_k: +(DEGREES.reduce((s, d) => s + d.avg_salary_k, 0) / n).toFixed(1),
    avg_ai_risk: +(DEGREES.reduce((s, d) => s + d.ai_automation_risk_pct, 0) / n).toFixed(1),
    highest_paid: DEGREES.reduce((a, b) => a.avg_salary_k > b.avg_salary_k ? a : b),
    most_resilient: DEGREES.reduce((a, b) => a.ai_resilience_score > b.ai_resilience_score ? a : b),
    highest_employed: DEGREES.reduce((a, b) => a.employment_rate_pct > b.employment_rate_pct ? a : b),
  }
}
